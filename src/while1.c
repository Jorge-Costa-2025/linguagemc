#include <stdio.h>
#include <stdlib.h>


int main() {
int a = 1;
// criar a variavel do tipo int para quardar o numero inteiro
// que será usado no laço de repetição while
while (a <= 20) {
    printf("contei %d\n", a);
    // imprimir o valor de a na tela
    // e incrementar a variável a em 1
    a++;
    // incrementar a variável a em 1
}
return 0;

}
    
